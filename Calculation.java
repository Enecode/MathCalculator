package calculator;

public class Calculation {
    public int addNumber(int a, int b) {
        return a + b;
    }

    public int subtractNumber(int a, int b) {
        return  a - b;
    }

    public int multiplyNumber(int a, int b) {
        return a * b;
    }

    public int divideNumber(int a, int c) {
        return a / c;
    }
}
